#include<iostream>
using namespace std;

int main(){
     int a=6, b=4;
     cout<< "Operators in c++"<<endl;

    //  ARITHMATIC OPERATOR

    cout<<"The value of a + b is "<< a+b<<endl;
    cout<<"The value of a - b is "<< a - b<<endl;
    cout<<"The value of a * b is "<< a * b<<endl;
    cout<<"The value of a / b is "<<a / b<<endl;
    cout<<"The value of a % b is "<<a % b<<endl;
    cout<<"The value of a ++  is "<<  a ++ <<endl;
    cout<<"The value of a --  is "<<  a -- <<endl;
    cout<<"The value of ++a  is "<<  ++a <<endl;
    cout<<"The value of --a  is "<<  --a <<endl;


    // ASSIGNMENT OPERATOR

    

}